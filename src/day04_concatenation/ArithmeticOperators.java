package day04_concatenation;

public class ArithmeticOperators {

    public static void main(String[] args) {

        System.out.println(100+200);
        System.out.println(100-40);
        System.out.println(20/6); // shows only integer number int/int => int value
        System.out.println(20/6.0);// int/ double = double value
        System.out.println(20d/6); // can put d to make int to double
        System.out.println((int)(25.0/5.0));// using casting
        System.out.println(10*2);
        System.out.println(15%4);

    }
}
